# Aritméticos
suma = 3 + 2
print(f"Esta es la suma: {suma}")
resta = 5 - 1
print(f"Esta es la resta: {resta}")
multi = 3 * 4
print(f"Esta es la multiplicación: {multi}")
div = 10 / 2
print(f"Esta es la división: {div}")
div_entera = 10 // 3
print(f"Esta es el entero: {div_entera}")
modulo = 10 % 3
print(f"Esta es el resto: {modulo}")
potencia = 2 ** 3
print(f"Esta es la potencia: {potencia}")

# Comparación
print(5 == 5)     # True
print(5 != 3)     # True
print(4 > 2)      # True
print(3 <= 4)     # True

# Lógicos
a = True
b = False
print(a and b)    # False
print(a or b)     # True
print(not a)      # False

# Asignación
x = 10
x += 5   # x = 15
x *= 2   # x = 30

